a = 15 * 3
print(type(a), a) #int
print(isinstance(a, int)) #true

b = 15 / 3
print(type(b), b) #float
print(isinstance(b, int)) #false

c = 15 // 2
print(type(c), c) #int
print(isinstance(c, int)) #true

d = 15 ** 2
print(type(d), d) #int
print(isinstance(d, int)) #true

